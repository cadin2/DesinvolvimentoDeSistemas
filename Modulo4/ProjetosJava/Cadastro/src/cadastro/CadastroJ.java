    private void btPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPActionPerformed
        String nome = nomeT.getText().trim();
        String endereco = enderecoT.getText().trim();
        String cpf = cpfT.getText().trim();
        String sexo = (String) sexoT.getSelectedItem();
        String curso = cursoT.getText().trim();
        String matricula = matriculaT.getText().trim();
        
        if(nome.isEmpty() || endereco.isEmpty() || cpf.isEmpty() || curso.isEmpty() || matricula.isEmpty()){
            JOptionPane.showMessageDialog(this,
                    "Ã© obrigatorio preencher todos os campos",
                    "campos obrigatorios",
                    JOptionPane.WARNING_MESSAGE);
        }
        
        Aluno alun = new Aluno(curso, matricula, 0, nome, endereco, sexo, cpf);
        
        try {
            alunDAO.criarPessoa(alun);
            JOptionPane.showMessageDialog(this,
                    "Aluno criado com sucesso!!",
                    "Sucesso!!",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Nao foi possivel criar o aluno",
                    "erro: "+e.getMessage(),
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btPActionPerformed

